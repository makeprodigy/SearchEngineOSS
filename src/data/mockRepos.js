// Helper functions
export const getPopularRepos = () => {
  return [...mockRepos].sort((a, b) => b.stars - a.stars).slice(0, 6);
};

export const getTrendingRepos = () => {
  return [...mockRepos].sort((a, b) => b.commits.lastWeek - a.commits.lastWeek).slice(0, 6);
};

export const getReposByTechStack = (techStack) => {
  if (!techStack || techStack.length === 0) return [];
  
  return mockRepos.filter(repo => 
    repo.topics.some(topic => 
      techStack.some(tech => 
        topic.toLowerCase().includes(tech.toLowerCase()) || 
        tech.toLowerCase().includes(topic.toLowerCase())
      )
    )
  ).slice(0, 6);
};

export const getRepoById = (id) => {
  return mockRepos.find(repo => repo.id === id);
};

